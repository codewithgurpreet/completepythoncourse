course = "Python Programming"

# What if you want to insert a double quote in your string?
# There are two ways:

# First one - use single quotes for the string
course = 'Python "Programming'
# Second one - use \ escape character
course = "Python \"Programming"
print(course)

# Other common escape sequences
# \"
course = "Python \"Programming"
print(course)
# \'
course = "Python \'Programming"
print(course)
# \\
course = "Python \\Programming"
print(course)
# \n
course = "Python \nProgramming"
print(course)