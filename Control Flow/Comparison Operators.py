# Comparison Operators are used to compare values
# Common comparison operators
# >
# <
# <=
# >=
# ==
# !=

# Comparison operators result in Boolean Expressions - Result is True or False
# Examples
10 > 3 <- True
10 >= 3 <- True
10 < 20 <- True
10 <= 20 <- True
10 == 10 <- True
10 == "10" <- False
10 != "10" <- True

# We can also use these comparison operators with strings
"bag" > "apple" <- True
"bag" == "BAG"  <- False
# The reason is that b and B are different
# check using the ord() function

ord("b") <- 98
ord("B") <- 66



